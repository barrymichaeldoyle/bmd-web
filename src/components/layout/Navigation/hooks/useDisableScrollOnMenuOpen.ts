import { useEffect } from "react";

export function useDisableScrollOnMenuOpen(isMenuOpen: boolean) {
  useEffect(() => {
    if (isMenuOpen) {
      document.body.style.overflow = "hidden";
    } else {
      document.body.style.overflow = "";
    }

    return () => {
      document.body.style.overflow = "";
    };
  }, [isMenuOpen]);
}
